# input function is a built-in function in python
# We can use it to read a value from terminal window

name = input("What is your name? ")
# We have added a space after question mark so that there is some space between our string and cursor in terminal window.
# input function reads the value from the terminal window and returns it.
# So, we can store it in a variable and then use it to greet our user.

print("Hello " + name)
# '"Hello " + name' - This is called concatenation of string.